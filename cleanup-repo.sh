# Function to safely remove from git
remove_from_git() {
    local file=$1
    if git ls-files --error-unmatch "$file" > /dev/null 2>&1; then
        echo -e "${YELLOW}Removing from git:${NC} $file"
        git rm --cached "$file"
    fi
}

